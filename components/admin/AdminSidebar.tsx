export default function AdminSidebar() {
  const pathname = usePathname()
  const [mobileOpen, setMobileOpen] = useState(false)

  /** Determine si un lien est actif */
  const isActive = (href: string): boolean => {
    if (href === '/dashboard') {
      return pathname === '/dashboard'
    }
    return pathname.startsWith(href)
  }

  /** Contenu de la sidebar (partage desktop/mobile) */
  const sidebarContent = (
    <>
      {/* Logo TOKOSSA */}
      <div className="mb-8">
        <Link
          href="/dashboard"
          className="text-2xl font-bold"
          onClick={() => setMobileOpen(false)}
        >
          <span className="text-primary-500">TO</span>
          <span className="text-white">KOSSA</span>
        </Link>
        <p className="text-sm text-gray-400 mt-1">Administration</p>
      </div>

      {/* Navigation */}
      <nav className="space-y-1 flex-1">
        {navLinks.map((link) => {
          const active = isActive(link.href)
          return (
            <Link
              key={link.href}
              href={link.href}
              onClick={() => setMobileOpen(false)}
              className={`flex items-center gap-3 px-4 py-3 rounded-lg transition-colors ${
                active
                  ? 'bg-primary-500 text-white font-semibold'
                  : 'text-gray-300 hover:bg-white/10 hover:text-white'
              }`}
            >
              {link.icon}
              {link.label}
            </Link>
          )
        })}
      </nav>

      {/* Liens secondaires (reglages) */}
      <div className="mt-6 pt-4 border-t border-white/10 space-y-1">
        {secondaryLinks.map((link) => {
          const active = isActive(link.href)
          return (
            <Link
              key={link.href}
              href={link.href}
              onClick={() => setMobileOpen(false)}
              className={`flex items-center gap-3 px-4 py-2.5 rounded-lg transition-colors text-sm ${
                active
                  ? 'bg-white/15 text-white font-semibold'
                  : 'text-gray-400 hover:bg-white/10 hover:text-white'
              }`}
            >
              {link.icon}
              {link.label}
            </Link>
          )
        })}
      </div>

      {/* Actions en bas */}
      <div className="mt-auto pt-4 border-t border-white/10 space-y-1">
        <Link
          href="/"
          onClick={() => setMobileOpen(false)}
          className="flex items-center gap-3 px-4 py-3 rounded-lg text-gray-400 hover:bg-white/10 hover:text-white transition-colors"
        >
          <svg className="w-5 h-5" fill="none" viewBox="0 0 24 24" stroke="currentColor">
            <path
              strokeLinecap="round"
              strokeLinejoin="round"
              strokeWidth={2}
              d="M10 19l-7-7m0 0l7-7m-7 7h18"
            />
          </svg>
          Retour boutique
        </Link>

        <button
          onClick={() => signOut({ callbackUrl: '/login' })}
          className="flex items-center gap-3 px-4 py-3 rounded-lg text-gray-400 hover:bg-red-500/20 hover:text-red-400 transition-colors w-full"
        >
          <svg className="w-5 h-5" fill="none" viewBox="0 0 24 24" stroke="currentColor">
            <path
              strokeLinecap="round"
              strokeLinejoin="round"
              strokeWidth={2}
              d="M17 16l4-4m0 0l-4-4m4 4H7m6 4v1a3 3 0 01-3 3H6a3 3 0 01-3-3V7a3 3 0 013-3h4a3 3 0 013 3v1"
            />
          </svg>
          Deconnexion
        </button>
      </div>
    </>
  )

  return (
    <>
      {/* Sidebar desktop */}
      <aside className="fixed left-0 top-0 bottom-0 w-64 bg-secondary-500 text-white p-4 hidden md:flex md:flex-col z-40">
        {sidebarContent}
      </aside>

      {/* Header mobile avec hamburger */}
      <header className="md:hidden bg-secondary-500 text-white p-4 flex items-center justify-between sticky top-0 z-50">
        <Link href="/dashboard" className="text-xl font-bold">
          <span className="text-primary-500">TO</span>
          <span className="text-white">KOSSA</span>
          <span className="text-gray-400 text-sm ml-2">Admin</span>
        </Link>

        <button
          onClick={() => setMobileOpen(!mobileOpen)}
          className="p-2 rounded-lg hover:bg-white/10 transition-colors"
          aria-label={mobileOpen ? 'Fermer le menu' : 'Ouvrir le menu'}
        >
          {mobileOpen ? (
            <svg className="w-6 h-6" fill="none" viewBox="0 0 24 24" stroke="currentColor">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M6 18L18 6M6 6l12 12" />
            </svg>
          ) : (
            <svg className="w-6 h-6" fill="none" viewBox="0 0 24 24" stroke="currentColor">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M4 6h16M4 12h16M4 18h16" />
            </svg>
          )}
        </button>
      </header>

      {/* Overlay mobile */}
      {mobileOpen && (
        <div className="md:hidden fixed inset-0 z-40">
          {/* Fond sombre cliquable */}
          <div
            className="absolute inset-0 bg-black/50"
            onClick={() => setMobileOpen(false)}
          />

          {/* Panneau lateral mobile */}
          <aside className="absolute left-0 top-0 bottom-0 w-72 bg-secondary-500 text-white p-4 flex flex-col animate-slide-in-right">
            {sidebarContent}
          </aside>
        </div>
      )}
    </>
  )
}
